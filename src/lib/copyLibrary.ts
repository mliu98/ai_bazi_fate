// TIER 类型定义
export type TIER = 'TIER_S' | 'TIER_A' | 'TIER_B' | 'TIER_C' | 'TIER_D';

export function getTIER(score: number): TIER {
  if (score >= 90) return 'TIER_S';
  if (score >= 80) return 'TIER_A';
  if (score >= 70) return 'TIER_B';
  if (score >= 60) return 'TIER_C';
  return 'TIER_D';
}

