// ─── 辅助工具 ─────────────────────────────────────────────────────────────────
function pick<T>(arr: T[], n: number): T[] {
  const copy = [...arr];
  const result: T[] = [];
  while (result.length < n && copy.length > 0) {
    const idx = Math.floor(Math.random() * copy.length);
    result.push(copy.splice(idx, 1)[0]);
  }
  return result;
}

function pickFirst<T>(arr: T[], n: number): T[] {
  return arr.slice(0, n);
}

